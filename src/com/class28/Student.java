package com.class28;

public class Student {
	private String name;
	private int age;
	
	//create setters to set the value
	
	public void setName(String name) {
		this.name = name;
	}
	public void setAge(int age) {
		this.age = age;
		
	}
	//create getters to give the value of the variable
	
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}

}
