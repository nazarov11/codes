package com.class23_2;

public class MainMethodOverload {
	public static void main(String[] args) {
		System.out.println("I am main method with string array argument");
	}

	public static void main() {
		System.out.println("I am main method with no parameters");
	}
	public static void main(String args) {
		System.out.println("I am main method with string argument");

	}
	public static void main(int a) {
		System.out.println("I am main method with int");

	}

}
